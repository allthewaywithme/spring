package com.Basics;

public class Main {
    public static void main(String[] args) {
        //java里唯一的三元运算符。。。
        // 这的符号的意思是:如果?前面的式子为真，那么就返回冒号:左边的值;否则返回右边的值。。
        int a=2;
        int b=4;
        System.out.println( (a >= b) ? a : b);
        for (int j = 0b0; j<10;j++){
            System.out.println(Math.random()*100);

        }
    }
}
